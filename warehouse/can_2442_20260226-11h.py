def print_colored(text, color_code):
    """Print text in color using ANSI escape codes"""
    print(f"\033[{color_code}m{text}\033[0m")

def typewriter(text, delay=0.05):
    """Print text with typewriter effect"""
    for char in text:
        sys.stdout.write(char)
        sys.stdout.flush()
        time.sleep(delay)
    print()

