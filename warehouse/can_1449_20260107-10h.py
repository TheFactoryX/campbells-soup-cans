def slow_print(text, delay=0.03, color_code="\033[97m"):
    """Prints text character by character with a delay and color."""
    print(color_code, end="", flush=True)
    for char in text:
        print(char, end="", flush=True)
        time.sleep(delay)
    print("\033[0m") # Reset color

