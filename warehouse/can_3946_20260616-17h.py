def clear_screen():
    sys.stdout.write("\033[2J\033[H")
    sys.stdout.flush()

def color_text(text, color_code):
    return f"\033[1;{color_code}m{text}\033[0m"

def typewriter(text, delay=0.05):
    for char in text:
        sys.stdout.write(char)
        sys.stdout.flush()
        time.sleep(delay)
    print()

def draw_frame(quote, width=70):
    """Creates a decorative neurotic-looking frame for the quote."""
    top_bottom = color_text("═" * (width + 2), "36")
    side = color_text("║", "36")
    
    # Border top
    print(f"\n{top_bottom}")
    
    # Content Area
    padding = (width - len(quote)) // 2
    if padding < 1:
        padding = 1
    
    # Calculate padding for left and right to center
    left_pad = " " * padding
    right_pad = " " * (width - len(quote) - padding)
    
    # Print Quote line
    print(f"{side}{left_pad}{color_text(quote, '93')} {right_pad}{side}")
    
    # Border bottom
    print(f"{top_bottom}\n")

